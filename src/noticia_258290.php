          <p class="text-justify"><p style="text-align: justify;">
	<span style="font-size:20px;">La empresa Aerol&iacute;neas Argentinas anunci&oacute; la oferta de vuelos para el reinicio progresivo de sus operaciones regulares, que comenzar&aacute;n este jueves 22 con el trayecto Ezeiza-Jujuy, informaron hoy fuentes de la compa&ntilde;&iacute;a.</span></p>
<p style="text-align: justify;">
	<span style="font-size:20px;">Luego de ese primer vuelo regular del jueves, se cubrir&aacute;n ese d&iacute;a los trayectos a Corrientes, Iguaz&uacute;, Mendoza, San Luis y Ushuaia, con lo que se completar&aacute; la programaci&oacute;n de la joranda, tras la habilitaci&oacute;n del Ministerio de Transporte, reglamentada por la Administraci&oacute;n Nacional de Aviaci&oacute;n Civil (ANAC) el jueves pasado.</span></p>
<p style="text-align: justify;">
	<span style="font-size:20px;">Para el viernes se anunciaron vuelos hacia y desde C&oacute;rdoba, La Rioja, Salta, Resistencia, Posadas, Bariloche, Comodoro Rivadavia y Neuqu&eacute;n.</span></p>
<p style="text-align: justify;">
	<span style="font-size:20px;">El resto de los destinos se incorporar&aacute;n de manera escalonada y las frecuencias se incrementar&aacute;n de forma paulatina y en funci&oacute;n de la demanda.</span></p>
<p style="text-align: justify;">
	<span style="font-size:20px;">Las fuentes reiteraron que, en todos los casos, los vuelos est&aacute;n sujetos a la aprobaci&oacute;n de los Gobiernos provinciales, que pueden deshabilitarlos si lo consideran conveniente.</span></p>
<p style="text-align: justify;">
	<span style="font-size:20px;">Cada provincia fija los requerimientos espec&iacute;ficos para el ingreso, m&aacute;s all&aacute; de lo estipulado como regla general por las autoridades aeron&aacute;uticas.</span></p>
<p style="text-align: justify;">
	<span style="font-size:20px;">La compa&ntilde;&iacute;a public&oacute; en su sitio web la programaci&oacute;n detallada por destino, y los requisitos generales para el embarque: https://www.aerolineas.com.ar/landingsespeciales/landings/892_vuelos-nacionales. Los pasajeros podr&aacute;n tramitar su permiso de viaje en: https://www.argentina.gob.ar/circular</span></p>
<p style="text-align: justify;">
	<span style="font-size:20px;">Durante las restricciones por la pandemia, Aerol&iacute;neas Argentinas oper&oacute; una gran cantidad de vuelos bajo la modalidad de &ldquo;especiales&rdquo; con un protocolo de bioseguridad estricto, adaptado a las regulaciones y recomendaciones de salud de los organismos de control nacionales e internacionales.</span></p>
<p style="text-align: justify;">
	<span style="font-size:20px;">La compa&ntilde;&iacute;a oper&oacute; con un casi nulo &iacute;ndice de contagiosidad entre sus pasajeros y sus trabajadores.</span></p>
